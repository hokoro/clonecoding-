#관계 연산자 : 두 피연산자의 관계를 나타내는 구문
print(a==b)
print(a!=b)
print(a<b)
print(a>b)
print(a<=b)
print(a>=b)
#증감 연산자: 한개나 두개 이상의 피연산자를 증가시키거나 감소시킬떄 사용하는 연산자
#c/c++ 처럼 ++ -- 는 python 에서 사용할수가 없다
c+= 10
print(c)
c-= 10
print(c)
c = a+b
print(c)
#논리 연산자 : 한개 혹은 두개 이상의 피연산자의 논리적 연산을 따지고 싶을떄 사용하는 구문
print(a and b)
print(a or b)
print(not a)
